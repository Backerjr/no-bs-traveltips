class StorytellerAgent:
    """
    Creates poetic, sensory-rich descriptions for experiences.
    Voice: magazine-style storytelling — emotional but elegant.
    """

    def describe(self, experience):
        return (
            f"Imagine yourself in {experience} — "
            f"the air warm, the colors vivid, the moment cinematic. "
            f"Luxury, not loudness — just that quiet wow that stays with you."
        )
