class ConciergeAgent:
    """
    Handles guest guidance, timing, dress codes, and comfort tips.
    """

    def advise(self, topic):
        return (
            f"For {topic}, I’d recommend keeping it simple — "
            f"comfortable clothing, light layers, and an unhurried pace. "
            f"That’s how you experience it like a local, not a tourist."
        )
